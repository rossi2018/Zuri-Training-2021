#Conver base of Number in integar 

#Base 2 - Binary
#Use 0b (zero+lower letter 'b') or 0B(zero + upper letter 'B') to convert to binary
print(0b10)
print(0B100)
print(0b100000)

#Base 8 - octal
#Use 0o(zero + lowercase letter'o') or 0O(zero + uppercase letter 'O')
print(0o10)
print(0O50)
print(0o500)

#Base 16 - Hexadecimal 
#Use 0x(zero + lowercase letter 'x') or 0X(zero + uppercase letter 'X')
print(0x15)
print(0X300)
print(0x500)
print(0x21)
